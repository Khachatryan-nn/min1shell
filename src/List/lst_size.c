#include "list.h"

/**
 * @brief Counts the number of nodes in a list.
 * 
 * @param lst A Linked List.
 * @return int The length of the list.
 */
int	lst_size(t_list *lst)
{
	int	size;

	size = 0;
	while (lst)
	{
		size++;
		lst = lst->next;
	}
	return (size);
}
