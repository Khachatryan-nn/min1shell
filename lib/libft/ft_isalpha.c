#include "libft.h"

/*
*	Reference: <ctype.h>
*
*	Description: Checks for an alphabetic cheracter.
*
*	Return Value: Nonzero if the character falls
*	into the tested class, and zero if not.
*/
int	ft_isalpha(int c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (0x400);
	return (0);
}
